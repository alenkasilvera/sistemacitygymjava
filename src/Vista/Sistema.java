    private void txtCodigoPagoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoPagoKeyPressed
        // TODO add your handling code here:

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(txtCodigoPago.getText())) {
                String cod = txtCodigoPago.getText();
                pro = proDao.BuscarPro(cod);
                if (pro.getNombre() != null) {
                    txtDescripcionPago.setText(""+pro.getNombre());
                    txtPrecioPago.setText(""+pro.getPrecio());
                    txtCuposPago.setText(""+pro.getCupos());
                    txtCantidadPago.requestFocus();
                } else { 
                    LimpiarPago();
                    txtCodigoPago.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el codigo del producto.");
                txtCodigoPago.requestFocus();
            }
        }


    }//GEN-LAST:event_txtCodigoPagoKeyPressed

    private void txtCantidadPagoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadPagoKeyPressed
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        if (!"".equals(txtCantidadPago.getText())) { // Corrige el operador de comparación
            String cod = txtCodigoPago.getText();
            String descripcion = txtDescripcionPago.getText();
            int cant = Integer.parseInt(txtCantidadPago.getText());
            double precio = Double.parseDouble(txtPrecioPago.getText());
            double total = cant * precio;
            int cupos = Integer.parseInt(txtCuposPago.getText());
            
            if (cupos >= cant) {
                item = item + 1;
                tmp = (DefaultTableModel) TablePago.getModel();
                
                for (int i = 0; i < TablePago.getRowCount(); i++) {
                    if (TablePago.getValueAt(i, 1).equals(txtDescripcionPago.getText())) {
                        JOptionPane.showMessageDialog(null, "El plan ya esta registrado.");
                        return;
                    }
                }
                
                
                // Usamos generics para definir el tipo de ArrayList
                ArrayList lista = new ArrayList();
                lista.add(item);
                lista.add(cod);
                lista.add(descripcion);
                lista.add(cant);
                lista.add(precio);
                lista.add(total);

                Object[] O = new Object[5];
                O[0] = lista.get(1);
                O[1] = lista.get(2);
                O[2] = lista.get(3);
                O[3] = lista.get(4);
                O[4] = lista.get(5); 
                
                tmp.addRow(O);
                TablePago.setModel(tmp);
                TotalPagar();
                LimpiarPago();
                txtCodigoPago.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Cupos no disponibles.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese cantidad.");
        }
    }
        
    }//GEN-LAST:event_txtCantidadPagoKeyPressed

