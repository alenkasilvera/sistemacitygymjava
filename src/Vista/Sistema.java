     private void TotalPagar() {
         Totalpagar = 0.00;
         int numFila = TablePago.getRowCount();
         for (int i = 0; i < numFila; i++) {
             double cal = Double.parseDouble(String.valueOf(TablePago.getModel().getValueAt(i, 4)));
             Totalpagar = Totalpagar + cal;
         }
         LabelTotal.setText(String.format("%.2f", Totalpagar));
     }
     
