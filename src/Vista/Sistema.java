    public void ListarSocio() {
        List<Socio> ListarCl = socio.ListarSocio();
        modelo = (DefaultTableModel) TableSocio.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < ListarCl.size(); i++) {
            ob[0] = ListarCl.get(i).getId();      // ID
            ob[1] = ListarCl.get(i).getDni();     // DNI
            ob[2] = ListarCl.get(i).getNombre();   // Nombre
            ob[3] = ListarCl.get(i).getTelefono(); // Teléfono
            ob[4] = ListarCl.get(i).getDireccion(); // Dirección
            modelo.addRow(ob);
        }

        TableSocio.setModel(modelo);

    }

